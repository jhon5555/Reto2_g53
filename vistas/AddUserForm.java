    private void btnConfirmActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConfirmActionPerformed
        String nombre = txtNombre.getText();
        String apellidos = txtApellidos.getText();
        int tipoDocumento = cbTipoDocumento.getSelectedIndex();
        String documento = txtDocumento.getText();
        String email = txtEmail.getText();
        String tipoDocumentoString = "";
        if(tipoDocumento == 0)
             tipoDocumentoString = " Cedula de cuidadania";
        else if(tipoDocumento == 1)
            tipoDocumentoString = "Cedula extranjera";
        else if(tipoDocumento == 2)
            tipoDocumentoString = "Libreta militar";
        else if(tipoDocumento == 3)
            tipoDocumentoString = "Pasaporte";
        else
            tipoDocumentoString = "Otro";
        System.out.println("Nombre: " + nombre + " " + apellidos 
                + ", documento: " + tipoDocumentoString +  " " + documento
                + ", correo: " + email);
        
        if (nombre.isEmpty() || apellidos.isEmpty() || documento.isEmpty() || email.isEmpty()){
            JOptionPane.showInputDialog(this,  "Hay campos requeridos sin diligenciar", "Formulario registro", JOptionPane.WARNING_MESSAGE);
        

        }else{
             String queryCrearEmpleado = 
                    "INSERT INTO `empleados`(`nombreEmp`, `apellidos`, `tipoDocumento`, `documento`, `correo`) VALUES ('" 
                    + nombre + "','" + apellidos + "','" + tipoDocumentoString + "','" 
                    + documento + "','" + email + "')";

            JOptionPane.showMessageDialog(this, "Registro éxitoso", "Nuevo Empleado",JOptionPane.WARNING_MESSAGE);
            try{
            connection = conexion.getConnection();
            st = connection.createStatement();
            st.executeUpdate(queryCrearEmpleado);
            
            }catch(SQLException e){
            JOptionPane.showMessageDialog(this," No se pudo crear el empleado", "Nuevo empleado",JOptionPane.ERROR_MESSAGE);
            }
            this.dispose();
        }

    }//GEN-LAST:event_btnConfirmActionPerformed

